#include <iostream>

using std::cout;
using std::endl;

int main()
{
    //used as commands to do stuff
    cout << "Hello\nYou" << endl;//\n - new line
    cout << "Hello\tMe" << endl;//\t - TAB
    cout << "Helloo\b" << endl;//\b - Backspace
    cout << "hello\vpeople" << endl;//\v - vertical TAB
    cout << "Hola\a" << endl;//\a - makes beep other terminal not vsc
    cout << "Hello \"there\"" << endl;//\"- types quote mark or single quote mark or backslash
    //And a buch of others.
}